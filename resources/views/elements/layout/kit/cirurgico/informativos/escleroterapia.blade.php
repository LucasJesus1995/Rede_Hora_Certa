<h1 class="title">Escleroterapia de varizes dos membros inferiores</h1>

<div class="font-contrato-size" style="">
    <br /><strong>O que é:</strong>
    <div style="padding-top:  5px"> Procedimento clínico, realizado por médico Angiologista ou Cirurgião Vascular.<br />Consiste na utilização de agente esclerosante químico para o tratamento de varizes dos membros inferiores. Os esclerosantes utilizados são a glicose hipertônica 75% e o polidocanol, na forma liquida ou de espuma.</div>

    <br /><br /><strong>Como é feito?</strong>
    <div style="padding-top:  5px"> O agente esclerosante é injetado diretamente no vaso a ser tratado. Ele produz uma reação inflamatória na parede da veia e a consequente fibrose, fazendo com que o vaso se feche.</div>

    <br /><br /><strong>Qual o objetivo do tratamento? O tratamento é efetivo?</strong>
    <div style="padding-top:  5px"> O objetivo desse tratamento é a melhora da circulação sanguínea. É tratado a insuficiência venosa superficial (varizes), e para essa finalidade é efetivo.</div>

    <br /><br /><strong>Quais os cuidados após o procedimento?</strong>
    <div style="padding-top:  5px"> 
        <ul>
            <li style="padding-left: 20px"> - Recomendamos o uso de meias elásticas.</li>
            <li style="padding-left: 20px"> - Evitar exposição ao sol.</li>
            <li style="padding-left: 20px"> - Evitar grandes esforços na primeira semana.</li>
            <li style="padding-left: 20px"> - Não é necessário o repouso absoluto, sendo recomendado andar desde o primeiro dia.</li>
            <li style="padding-left: 20px"> - Se necessário para a recuperação, será fornecido atestado médico.</li>
        </ul>
    </div>

    <br /><br /><strong>O resultado é imediato?</strong>
    <div style="padding-top:  5px"> O resultado não é imediato. As veias tratadas podem sofrer uma inflamação e irão diminuir com o passar das semanas. Podem inicialmente ficar endurecidas e avermelhadas. Áreas acastanhadas podem surgir no local das aplicações. Essas alterações vão melhorando com o passar das semanas. Porém é importante observar que cada pessoa responde de uma maneira.</div>
    <div style="padding-top:  5px"> Pode ser necessário novas aplicações. Durante todo o tratamento, o paciente será acompanhado por um médico vascular.</div>


    <br /><br /><strong>Qual a vantagem da Escleroterapia?</strong>
    <div style="padding-top:  5px"> O tratamento das varizes com escleroterapia tem como vantagem não necessitar de internação hospitalar. É um procedimento ambulatorial, ou seja, realizado em consultório médico.</div>


    <br /><br /><strong>Existem complicações?</strong>
    <div style="padding-top:  5px"> Como todo procedimento médico, complicações podem ocorrer. Assim para maior segurança do procedimento é importante a avaliação do Médico Vascular e o seguimento correto das orientações pós procedimento.</div>
    <div style="padding-top:  5px"> As principais complicações que podem ocorrer na escleroterapia são hiperpigmentação e aumento da sensibilidade da pele, inchaço (edema), trombose, alergia ao medicamento e infecção.</div>

</div>


